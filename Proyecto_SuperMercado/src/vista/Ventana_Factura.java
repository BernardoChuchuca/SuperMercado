    public Double iva(String iva,double precio){
        double ivaa=0;
        if(iva.equals("S")){
            ivaa=(precio*12)/(100);
            
        }else {
          ivaa=0;
        }
        
        return ivaa;
    };
    
    public Double Descuento(double precio, double desc ){
        double descuento=0;
        
        if(desc!=0){
         descuento=(precio*desc)/(100);
        
    }
        
    return descuento;
    } 
