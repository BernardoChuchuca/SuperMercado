    public int  codigo(ArrayList<Integer>lis){
      boolean n=false;
      int num=1;
        while (n==false) {      
             num=new Random().nextInt(100)+0;
            if(!new GestionFactura().comparar(num, lis)){
               n=true;
            
            }
        }

      return num;
     }
    
